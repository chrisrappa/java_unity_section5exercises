public class FirstSwitch {

  public static String whichChar(char newChar){

    switch(newChar){
      case 'a':
        return("It was a");
      case 'b':
        return("It was b");
      case 'c':
        return("It was c");
      case 'd':
        return("It was d");
      case 'e':
        return("It was e");
      default:
        return("Invalid char");
    }
  }

}
